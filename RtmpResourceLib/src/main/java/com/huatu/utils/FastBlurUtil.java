	public static float DEFAULT_DOWNSCALEFACTOR = 5.0f;

	public static Bitmap scaleBitmap(Bitmap myBitmap) {

		int width = (int) (myBitmap.getWidth() / DEFAULT_DOWNSCALEFACTOR);
		int height = (int) (myBitmap.getHeight() / DEFAULT_DOWNSCALEFACTOR);

		return Bitmap.createScaledBitmap(myBitmap, width, height, false);
	}

